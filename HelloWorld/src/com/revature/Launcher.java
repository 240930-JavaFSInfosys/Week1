package com.revature;

public class Launcher {

    // This is the main method - the starting point for our application
    public static void main(String[] args) {

        // This is a print statement
        // It allows us to "print" a message to the console (the box that appears when we run the app)
        System.out.println("Hello World!");

        System.out.println("We are learning fundamental Java!");

        System.out.println("We can type \"sout\" to quickly generate a print statement!");

        System.out.println("Here's a new line");
        
        /* This is a block comment AKA multi-line comment

        System = This system. The console.
        out = output, we're defining something that will be outputted.
        println = prints a line to the console

        fun fact: if we just did System.out.print, they would all print on the same line */

    }

}
